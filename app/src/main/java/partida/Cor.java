package partida;

/**
 * Enumeração que representa as cores dos jogadores no jogo de xadrez.
 * 
 * A enumeração {@code Cor} possui dois valores possíveis:
 * - {@code PRETO}: Representa a cor preta, associada a um dos jogadores.
 * - {@code BRANCO}: Representa a cor branca, associada ao outro jogador.
 * 
 * Essas cores são usadas para identificar os jogadores e suas peças durante a partida.
 */
public enum Cor {
    PRETO, BRANCO,
}